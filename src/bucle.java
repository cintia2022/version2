public class bucle {

    public static void main(String[] args) {

     int cont = 0;                  /*variable entera que sea contador, inicio en 0 */

     while(cont<=10){          /*condicion, mientras el contador sea <= 10 se sigue ejecutando */

           System.out.println("con while estoy en la vuelta " + cont);   
           cont = cont + 1;      /*va sumando de 1 en 1 */

     }

}

}